    private void modelarTabela(){
        modelo.addColumn("ID");
        modelo.addColumn("NOME");
        modelo.addColumn("CPF");
        modelo.addColumn("RG");
        tblAlunos.setModel(modelo);
    }
    
     private void preencherTabelaAluno(int idInstrutor){
        modelo.setNumRows(0);
        for(Aluno a: new AlunoController().ListarPorInstrutor(idInstrutor)){
            modelo.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getCpf(),
                a.getRg(),
                a.getInstrutor().getNome() 
            });
        }
    }
