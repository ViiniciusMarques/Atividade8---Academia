    private void btnCadastrarInstrutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarInstrutorActionPerformed
        if(txtNomeInstrutor.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo NOME vazio!", "Aten√ß√£o!", JOptionPane.WARNING_MESSAGE);
        }
        else if(txtRgIntrutor.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo RG vazio!", "Aten√ß√£o!", JOptionPane.WARNING_MESSAGE);
        }
        else if(txtCpfInstrutor.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo CPF vazio!", "Aten√ß√£o!", JOptionPane.WARNING_MESSAGE);    
        }
        else if(txtCtpsInstrutor.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo CTPS vazio!", "Aten√ß√£o!", JOptionPane.WARNING_MESSAGE);
        }
        else if(txtCargaHorariaInstrutor.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo CARGA HOR√ÅRIA vazio!", "Aten√ß√£o!", JOptionPane.WARNING_MESSAGE);
        }
        else if(txtLoginInstrutor.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo LOGIN vazio!", "Aten√ß√£o!", JOptionPane.WARNING_MESSAGE);
        }
        else if(txtSenhaInstrutor.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo SENHA vazio!", "Aten√ß√£o!", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Instrutor i = new Instrutor();
            Usuario u = new Usuario();
            InstrutorController ic = new InstrutorController();
            UsuarioController uc = new UsuarioController();
            
            i.setNome(txtNomeInstrutor.getText());
            i.setRg(txtRgIntrutor.getText());
            i.setCpf(txtCpfInstrutor.getText());
            i.setCtps(txtCtpsInstrutor.getText());
            i.setCargaHoraria(Double.parseDouble(txtCargaHorariaInstrutor.getText()));
            u.setLogin(txtLoginInstrutor.getText());
            u.setSenha(txtSenhaInstrutor.getText());
            u.setNome_usuario(txtNomeInstrutor.getText());
            u.setTipo_usuario(tipoUsuario.Instrutor);
            uc.Cadastrar(u);
            i.setUsuario(u);
            ic.Cadastrar(i);
            
            JOptionPane.showMessageDialog(rootPane, "Cadastro realizado com sucesso");
            
            txtCargaHorariaInstrutor.setText("");
            txtCpfInstrutor.setText("");
            txtCtpsInstrutor.setText("");
            txtLoginInstrutor.setText("");
            txtNomeInstrutor.setText("");
            txtRgIntrutor.setText("");
            txtSenhaInstrutor.setText("");
            this.dispose();
        }
    }//GEN-LAST:event_btnCadastrarInstrutorActionPerformed

